def update_figure(df: pd.DataFrame = None, xaxis=None, yaxis=None, chart_type=None):
    if chart_type == "bar":
        fig = px.bar(df, x=xaxis, y=yaxis, title="Invalid")
        fig.layout.update(
            dict(
                showlegend=True,
                legend=dict(x=0, y=1.0),
                margin=dict(l=40, r=10, t=40, b=100),
            )
        )
        return fig
    elif chart_type == "scatter":
        newdf = df.groupby(xaxis).mean()
        fig = px.line(newdf, x=newdf.index, y=yaxis, title=f'{xaxis} x {yaxis}')
        fig.layout.update(
            dict(
                showlegend=True,
                legend=dict(x=0, y=1.0),
                margin=dict(l=40, r=10, t=40, b=100),
            )
        )
        return fig


def create_chart(story_id, chart_id):

    random_id = int(random.random() * 100) + 100
    chart_title = f"Sales Chart -{random_id}"

    chart_id_dict = {"type": "dynamic-chart", "index": story_id, "id": chart_id}
    f = bar
    if random_id %2 ==0:
        f = line


    graph = dcc.Graph(
        figure=f,
        style={"height": "90%"},
        id=chart_id_dict,
        responsive=True,
        config={
            "autosizable": True,
            "doubleClick": "autosize",
            "frameMargins": 0,
            "responsive": True,
            "watermark": False,
        },
    )

    # fl.register_vis(
    #     chart_title, graph,
    # )
    # json manipulation for children list
    card = gc.Card(graph, title=chart_title, card_id=chart_id, story_id=story_id)

    # card_json = json.dumps(card,cls=plotly.utils.PlotlyJSONEncoder)
    card_json = card.to_plotly_json()
    ls = card_json["props"]["children"]
    ls2 = [x.to_plotly_json() for x in ls]
    card_json["props"]["children"] = ls2

    # json manipulation for layouts
    layout_card = {
        "w": 3,
        "h": 2,
        "x": 0,
        "y": 0,
        "i": {"type": "fl-card", "index": story_id, "id": chart_id},
        "minW": 3,
        "minH": 2,
        "moved": False,
        "static": False,
    }

    return card_json, layout_card


